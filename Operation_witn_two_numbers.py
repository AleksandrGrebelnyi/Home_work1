# Exercise 4. Write a program that requests the user to enter two numbers and
# prints the sum, product, difference and quotient of the two numbers.

num1 = int(input('Print any number: '))
num2 = int(input('Print any number one more time: '))
print('Sum of two numbers =', num1 + num2)  # print(f'Sum of two numbers = {num1 + num2}')
print('Multiply of two numbers =', num1 * num2)
print('Difference of two numbers =', num1 - num2)
print('Quotient of two numbers =', num1 / num2)
